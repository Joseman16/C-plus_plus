// JMLC - Operadores aritméticos básicos

#include<iostream>

using namespace std;

int main(){
	int numEntero1 = 10, numEntero2 = 4;
	float numFlotante1 = 19.3, numFlotante2 = 3.7;
	
	cout<<"\n\Operaciones Aritmeticas de números Enteros"<<endl;
	cout<<"Suma: "<<numEntero1 + numEntero2 <<endl;
	cout<<"Resta: "<<numEntero1 - numEntero2 <<endl;
	cout<<"Multiplicación: "<<numEntero1 * numEntero2<<endl;
	cout<<"División: "<<numEntero1/numEntero2<<endl;
	
	cout<<"\n\Operaciones Aritmeticas de números Flotantes"<<endl;
	cout<<"Suma: "<<numFlotante1 + numFlotante2 <<endl;
	cout<<"Resta: "<<numFlotante1 - numFlotante2 <<endl;
	cout<<"Multiplicación: "<<numFlotante1 * numFlotante2<<endl;
	cout<<"División: "<<numFlotante1/numFlotante2<<endl;
	
	return 0; 
}

